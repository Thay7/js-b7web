let lista = ['macarrao', 'arroz', 'carne', 'sal']

//Key
console.log(Object.keys(lista)) //Retorna um array com os indices dos itens

//Value
console.log(Object.values(lista)) //Retorna um array com os itens

//Entries
console.log(Object.entries(lista)) //Retorna um array que em cada posição tem outro array contendo dois itens: o indice e o valor

